# src/evaluation.py
"""
Model Evaluation Script
-----------------------
This script evaluates the trained models on the test set using:
1. Accuracy Score
2. Precision Score
3. Recall Score
4. F1 Score
5. Confusion Matrix (plotted and saved)

Additionally, it extracts feature importance from the Random Forest model,
plots the top 15 features, and exports a model comparison CSV table.

All evaluation plots and data are saved under the 'outputs/' directory.
"""

import os
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns
from sklearn.metrics import accuracy_score, precision_score, recall_score, f1_score, confusion_matrix

def evaluate_models(X_test, y_test, lr_model, rf_model, preprocessor_pipeline, outputs_dir):
    """Evaluates Logistic Regression and Random Forest models and saves comparisons."""
    os.makedirs(outputs_dir, exist_ok=True)
    
    print("\n=== Model Evaluation ===")
    
    # 1. Transform testing features
    X_test_transformed = preprocessor_pipeline.transform(X_test)
    
    # 2. Make predictions
    y_pred_lr = lr_model.predict(X_test_transformed)
    y_pred_rf = rf_model.predict(X_test_transformed)
    
    # 3. Calculate metrics
    metrics = {
        'Logistic Regression': {
            'Accuracy': accuracy_score(y_test, y_pred_lr),
            'Precision': precision_score(y_test, y_pred_lr),
            'Recall': recall_score(y_test, y_pred_lr),
            'F1 Score': f1_score(y_test, y_pred_lr)
        },
        'Random Forest': {
            'Accuracy': accuracy_score(y_test, y_pred_rf),
            'Precision': precision_score(y_test, y_pred_rf),
            'Recall': recall_score(y_test, y_pred_rf),
            'F1 Score': f1_score(y_test, y_pred_rf)
        }
    }
    
    # Convert metrics to a neat DataFrame comparison table
    comparison_df = pd.DataFrame(metrics).T
    print("\n[+] Model Evaluation Comparison Table:")
    print(comparison_df.round(4))
    
    # Save comparison table to CSV
    comparison_path = os.path.join(outputs_dir, 'model_comparison.csv')
    comparison_df.to_csv(comparison_path)
    print(f"[+] Saved model comparison table to: {comparison_path}")
    
    # 4. Generate and Plot Confusion Matrices
    cm_lr = confusion_matrix(y_test, y_pred_lr)
    cm_rf = confusion_matrix(y_test, y_pred_rf)
    
    fig, axes = plt.subplots(1, 2, figsize=(12, 5))
    
    # Logistic Regression CM
    sns.heatmap(cm_lr, annot=True, fmt='d', cmap='Blues', ax=axes[0],
                xticklabels=['Team 2 Wins', 'Team 1 Wins'],
                yticklabels=['Team 2 Wins', 'Team 1 Wins'])
    axes[0].set_title("Logistic Regression Confusion Matrix")
    axes[0].set_xlabel("Predicted Label")
    axes[0].set_ylabel("True Label")
    
    # Random Forest CM
    sns.heatmap(cm_rf, annot=True, fmt='d', cmap='Greens', ax=axes[1],
                xticklabels=['Team 2 Wins', 'Team 1 Wins'],
                yticklabels=['Team 2 Wins', 'Team 1 Wins'])
    axes[1].set_title("Random Forest Confusion Matrix")
    axes[1].set_xlabel("Predicted Label")
    axes[1].set_ylabel("True Label")
    
    plt.tight_layout()
    cm_path = os.path.join(outputs_dir, 'confusion_matrices.png')
    plt.savefig(cm_path, dpi=150)
    plt.close()
    print(f"[+] Saved confusion matrices plot to: {cm_path}")
    
    # 5. Extract Feature Importance for Random Forest
    # We retrieve the feature names generated by the OneHotEncoder in the column transformer
    preprocessor = preprocessor_pipeline.named_steps['preprocessor']
    feature_names = preprocessor.get_feature_names_out()
    
    # Clean up feature names to make them look nice (e.g. removing 'cat__' prefixes)
    clean_feature_names = [name.replace('cat__', '') for name in feature_names]
    
    # Get importances from Random Forest
    importances = rf_model.feature_importances_
    
    # Create DataFrame of feature importances
    importance_df = pd.DataFrame({
        'Feature': clean_feature_names,
        'Importance': importances
    }).sort_values(by='Importance', ascending=False)
    
    # Print top 10 features to console
    print("\n[+] Top 10 Features Influencing Match Outcome (Random Forest):")
    print(importance_df.head(10).to_string(index=False))
    
    # Plot top 15 features and save
    plt.figure(figsize=(10, 6))
    top_15_features = importance_df.head(15)
    sns.barplot(x=top_15_features['Importance'], y=top_15_features['Feature'], palette="crest", hue=top_15_features['Feature'], legend=False)
    plt.title("Top 15 Feature Importances (Random Forest)")
    plt.xlabel("Gini Importance")
    plt.ylabel("Encoded Feature")
    plt.tight_layout()
    importance_plot_path = os.path.join(outputs_dir, 'feature_importance.png')
    plt.savefig(importance_plot_path, dpi=150)
    plt.close()
    print(f"[+] Saved feature importance plot to: {importance_plot_path}")
    
    return comparison_df

if __name__ == "__main__":
    # Test execution
    project_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    from data_ingestion import load_data, clean_data
    from preprocessing import preprocess_data
    from model_training import train_models
    
    data_path = os.path.join(project_dir, 'data', 'matches.csv')
    models_path = os.path.join(project_dir, 'models')
    out_dir = os.path.join(project_dir, 'outputs')
    try:
        raw_df = load_data(data_path)
        cleaned_df = clean_data(raw_df)
        X_tr, X_te, y_tr, y_te, pipe = preprocess_data(cleaned_df, models_path)
        lr_m, rf_m = train_models(X_tr, y_tr, pipe, models_path)
        evaluate_models(X_te, y_te, lr_m, rf_m, pipe, out_dir)
    except Exception as e:
        print(f"[-] Error in evaluation: {e}")
